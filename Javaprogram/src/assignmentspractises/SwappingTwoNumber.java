package assignmentspractises;

public class SwappingTwoNumber 
{

	public static void main(String[] args) 
	{
	 
		
		// Swapping two numbers without using 3rd variable and only arithmetic operators
		
		int a = 10,b=20;
		System.out.println(a);
		System.out.println(b);
		
		a+=10;b-=10;
		
		System.out.println(a);
		System.out.println(b);
		
		

	}

}
