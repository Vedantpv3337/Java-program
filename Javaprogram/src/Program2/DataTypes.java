package Program2;

public class DataTypes
{

	public static void main(String[] args) 
	{
		
		// Numeric DataTypes
		
		int a =100, b =200;
		System.out.println("The value of a is :"+a);
		System.out.println("The value of b is :"+b);
		System.out.println(a+b);
		
		System.out.println("The sum of value is :"+(a+b));  // + between 2 operants is operator & + between 2 Strings is "CONCATENATION"
		
		byte by = 125;
		System.out.println(by);
		
		short sh = 3467;
		System.out.println(sh);
		
		long l =12345678l;          // L or l (LITERALS)
		System.out.println(l);	
		
		// Decimal number --- Float and Double
		
		float itemprice = 20.46F;    // F or f (LITERALS)
		System.out.println(itemprice);
		
		double dbl = 235.45555D;    // D or d (LITERALS)
		System.out.println(dbl);
		
		char ch = 'A';
		System.out.println(ch);
		
		String str = "vedant";
		System.out.println(str);
		
		//char ch = 'ABC'; //INVALID
		//String st = 'aman'; //INVALID
		//String at = 'A';    //INVALID
		//String c = "A";      //VALID
		
		
		boolean bl = true;     //TRUE OR FALSE
		System.out.println(bl);
		
		

	}

}
