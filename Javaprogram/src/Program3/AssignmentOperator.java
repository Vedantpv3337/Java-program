package Program3;

public class AssignmentOperator 
{

	public static void main(String[] args)
	{
		
		// Assignment Operator = += -= *= /= %=

		// CASE 1
		/*
		 * int a=10; System.out.println(a); //10
		 * 
		 * a+=5; // a = a+5 System.out.println(a); //15
		 */

		// CASE 2
		/*
		 * int a = 10; a-=5; System.out.println(a); //5
		 */

		// CASE 3

		int a = 10;
		a *= 5;
		System.out.println(a); // 50

		// CASE 4
		/*
		 * int a = 10; a/=5; System.out.println(a); //2
		 */

		// CASE 5
		/*
		 * int a = 10; a%=5; System.out.println(a); //0
		 */
		
		
		        
		
	            

	}

}
